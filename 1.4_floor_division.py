# The problem
# Find the floor division of two numbers.

# Hints
# Floor division means the integer part of a division operation. For example, if you divide 17/5 the quotient will be 3.4. Here the integer part is 3. So, you have to find the integer part of the division operation.

# Solution
print(17 // 5)
# used two division operators to find the floor division of 17/5
